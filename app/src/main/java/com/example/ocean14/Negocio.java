package com.example.ocean14;

public class Negocio {

    String nombre;
    String servicio;
    String telefono;
    String domicilio;
    String hora;
    String facebook;
    String imagen;
    String telefono2;
    String whatsapp;
    String correo_electronico;
    String instagram;
    String youtube;
    String twitter;
    String ciudad;
    String codigo_postal;
    String colonia, estado, calle;

    public Negocio(String nombre, String servicio, String telefono, String domicilio, String hora, String facebook, String imagen, String telefono2, String whatsapp, String correo_electronico, String instagram, String youtube, String twitter, String ciudad, String codigo_postal, String colonia, String estado, String calle) {
        this.nombre = nombre;
        this.servicio = servicio;
        this.telefono = telefono;
        this.domicilio = domicilio;
        this.hora = hora;
        this.facebook = facebook;
        this.imagen = imagen;
        this.telefono2 = telefono2;
        this.whatsapp = whatsapp;
        this.correo_electronico = correo_electronico;
        this.instagram = instagram;
        this.youtube = youtube;
        this.twitter = twitter;
        this.ciudad = ciudad;
        this.codigo_postal = codigo_postal;
        this.colonia = colonia;
        this.estado = estado;
        this.calle = calle;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getServicio() {
        return servicio;
    }

    public void setServicio(String servicio) {
        this.servicio = servicio;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDomicilio() {
        return domicilio;
    }

    public void setDomicilio(String domicilio) {
        this.domicilio = domicilio;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getTelefono2() {
        return telefono2;
    }

    public void setTelefono2(String telefono2) {
        this.telefono2 = telefono2;
    }

    public String getWhatsapp() {
        return whatsapp;
    }

    public void setWhatsapp(String whatsapp) {
        this.whatsapp = whatsapp;
    }

    public String getCorreo_electronico() {
        return correo_electronico;
    }

    public void setCorreo_electronico(String correo_electronico) {
        this.correo_electronico = correo_electronico;
    }

    public String getInstagram() {
        return instagram;
    }

    public void setInstagram(String instagram) {
        this.instagram = instagram;
    }

    public String getYoutube() {
        return youtube;
    }

    public void setYoutube(String youtube) {
        this.youtube = youtube;
    }

    public String getTwitter() {
        return twitter;
    }

    public void setTwitter(String twitter) {
        this.twitter = twitter;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getCodigo_postal() {
        return codigo_postal;
    }

    public void setCodigo_postal(String codigo_postal) {
        this.codigo_postal = codigo_postal;
    }

    public String getColonia() {
        return colonia;
    }

    public void setColonia(String colonia) {
        this.colonia = colonia;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }
}
